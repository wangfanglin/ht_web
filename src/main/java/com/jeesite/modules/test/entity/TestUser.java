package com.jeesite.modules.test.entity;

public class TestUser implements BaseHistoryData {
    private String name ;
    private int old ;

    public TestUser(String name, int old) {
        this.name = name;
        this.old = old;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getOld() {
        return old;
    }

    public void setOld(int old) {
        this.old = old;
    }
}
